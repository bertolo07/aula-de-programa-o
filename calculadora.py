a=float(input())         #float = números decimais 
c=input()
b=float(input())           #int = números inteiros

if c=="+":                 
    print(a+b)              #depois de uma condição, como if no final ":"
elif c=="-": 
    print(a-b)
elif c=="*": 
    print(a*b)
elif c=="/": 
    print(a/b)
